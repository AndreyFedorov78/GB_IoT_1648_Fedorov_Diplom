int make_buff(char *buff, uint8_t operation) {

	char *old = buff;
	uint8_t i = 0;
	buff = buff_copy(buff, "ID;", 3);
	buff = long_to_buff(buff, id);
	switch (operation) {
	case 3:
		if (temp_on) {
			buff = buff_copy(buff, ";T;", 3);
			buff = long_to_buff(buff, (int16_t) temperature);
		}
		break;
	case 1:
		if (temp_on) {
			buff = buff_copy(buff, ";H;", 3);
			buff = long_to_buff(buff, (int16_t) humidity);
		}
		break;
	case 2:
		if (ppm > 300) {
			buff = buff_copy(buff, ";CO;", 4);
			buff = long_to_buff(buff, ppm);
		}
		break;
	}
	while (buff != old) {
		i++;
		old++;
	}
	return i;

}
